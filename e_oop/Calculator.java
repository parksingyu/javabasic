package e_oop;

public class Calculator {
	
	//두 값의 산술연산을 수행하는 메서드들을 만들어주세요.
	
	long plus(long a, long b){
		return a + b;
	}
	
	long minus(long a, long b){
		return a - b;
	}
	
	long multiple(long a, long b){
		return a * b;
	}
	
	long divide(long a, long b){
		return a / b;
	}
	
	long remain(long a, long b){
		return a % b;
	}

}